#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*b;
	size_t	i;

	i = 0;
	b = s;
	if (n == 0)
		return ;
	while (i < n)
	{
		b[i] = 0;
		i++;
	}
}
